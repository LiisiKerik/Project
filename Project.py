def Det_Gaussian_elimination(x):
  n = len(x)
  Det = 1
  for i in range(n):
    Nonzero = False
    for j in range(i, n):
      if x[j][i] != 0:
        Nonzero = True
        if j != i:
          Det = -Det
          for k in range(i, n):
            x[i][k], x[j][k] = x[j][k], x[i][k]
        break
    if Nonzero:
      Pivot = x[i][i]
      Det *= Pivot
      Inv_pivot = 1 / Pivot
      for j in range(i + 1, n):
        x[i][j] *= Inv_pivot
      for j in range(i + 1, n):
        for k in range(i + 1, n):
          x[j][k] -= x[j][i] * x[i][k]
    else:
      return(0)
  return(Det)
